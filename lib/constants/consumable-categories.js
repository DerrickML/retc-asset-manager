import { ENUMS } from "../appwrite/config";

export const CONSUMABLE_CATEGORY_GROUPS = {
  NREP: {
    [ENUMS.CONSUMABLE_SCOPE.PROJECT]: [
      ENUMS.CONSUMABLE_CATEGORY.APRONS,
      ENUMS.CONSUMABLE_CATEGORY.REFLECTORS,
      ENUMS.CONSUMABLE_CATEGORY.PROJECT_TEARDROPS,
      ENUMS.CONSUMABLE_CATEGORY.PROJECT_BANNERS,
      ENUMS.CONSUMABLE_CATEGORY.PROJECT_BOOTH_MATERIALS,
      ENUMS.CONSUMABLE_CATEGORY.PROJECT_UMBRELLAS,
      ENUMS.CONSUMABLE_CATEGORY.PROJECT_T_SHIRTS,
      ENUMS.CONSUMABLE_CATEGORY.PROJECT_BAGS,
    ],
    [ENUMS.CONSUMABLE_SCOPE.ADMIN]: [
      ENUMS.CONSUMABLE_CATEGORY.ADMIN_TP,
      ENUMS.CONSUMABLE_CATEGORY.ADMIN_SUGAR,
      ENUMS.CONSUMABLE_CATEGORY.ADMIN_WATER,
    ],
  },
  RETC: {
    [ENUMS.CONSUMABLE_SCOPE.ADMIN]: [
      ENUMS.CONSUMABLE_CATEGORY.FLIERS,
      ENUMS.CONSUMABLE_CATEGORY.TEARDROPS,
      ENUMS.CONSUMABLE_CATEGORY.BACKDROPS,
      ENUMS.CONSUMABLE_CATEGORY.INDOOR_BANNERS,
      ENUMS.CONSUMABLE_CATEGORY.OUTDOOR_BANNERS,
      ENUMS.CONSUMABLE_CATEGORY.MARKERS,
    ],
  },
};

export function getConsumableCategoriesForOrg(orgCode, scope) {
  const upperOrg = (orgCode || "").toUpperCase();
  const orgGroups = CONSUMABLE_CATEGORY_GROUPS[upperOrg];
  if (!orgGroups) return Object.values(ENUMS.CONSUMABLE_CATEGORY);
  const categories = orgGroups[scope];
  if (!categories || categories.length === 0) {
    return Object.values(ENUMS.CONSUMABLE_CATEGORY);
  }
  return categories;
}
